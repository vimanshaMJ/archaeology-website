			<h4>Postgraduate Diploma</h4>

            <p>Post Graduate Diploma students have to follow five courses. The courses; GGY 501, GGY 502 and GGY 505 are compulsory. In addition they must select two optional courses. The evaluation of each course consists of a final examination (carry 60% of the marks) and two assignments (carry 40% of the marks) written on individual selected, pertinent topics.</p>
            <p>To pass, students have to earn at least 50% (30 marks) in the final examination and 50% (20 marks) in the assignments in each course. Lectures in the Diploma programme are conducted in English and the total number of teaching hours is 35.An additional 10 hours of discussion are allowed in Sinhala / Tamil.</p>
